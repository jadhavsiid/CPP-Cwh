#include<iostream>
using namespace std;
int main()
{
    int num1,num2;
    cout<<"Enter the value of num1 : "; /* this is insertion operator */
    cin>>num1; /* this is an extraction operator*/
    cout<<"Enter the value of num2 : "; 
    cin>>num2; 
    cout<<"The sum is : "<<num1+num2;
    
    return 0;
}
