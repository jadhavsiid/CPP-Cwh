/* Learning Break & Continue */
#include<iostream>
using namespace std;
int main()
{
    
    for (int i = 0; i <=5 ; i++)
    {
        // cout<<i<<'\n';
        if (i==2)
        {
            //break;
            continue;
        }
        cout<<i<<'\n';
        
    }
    
    
    
    
    
    return 0;
}